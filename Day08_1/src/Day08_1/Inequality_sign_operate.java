package Day08_1;

public class Inequality_sign_operate {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int c = 30;
		System.out.println(a<b); //a가 b보다 작아?
		System.out.println(a>b); //a가 b보다 커?
		System.out.println(a+b<=c); //a+b가 c보다 작거나 같아?
		System.out.println(a+b>=c);
	}

}
